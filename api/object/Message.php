<?php

class Message {
    public $text;

    function __construct() {
        $this->text = "";
    }

    /**
     * @param string $text
     */
    public function setText($text)
    {
        $this->text = $text;
    }

    /**
     * @return string
     */
    public function getText()
    {
        return $this->text;
    }


}